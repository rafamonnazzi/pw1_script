var frutas  = new Array ('laranja', 'manga', 'abacaxi', 'uva', 'melancia');

frutas.push('banana') // push - adiciona.
frutas.push('goiaba')
console.log(frutas.join("|")) // entrar com caractere
console.log(frutas.pop()) //  pop - tira o ultimo (desmontar da ultima para a primeira)
console.log(frutas.shift()) // shift - tira o primeiro (desmontar da primeira para a ultima)




